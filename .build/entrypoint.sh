#!/bin/sh -l

set -e

input_paths="$1"

### Function Header ###########################################################
Header() {
  ##########
  # Prints #
  ##########
  echo "---------------------------------------------"
  echo "-- GitHub Actions TYPO3 Xliff Linter ---"
  echo "---------------------------------------------"
  echo "TYPO3 Xliff Linter source code can be found at:"
  echo " - https://github.com/TYPO3-Continuous-Integration/TYPO3-CI-Xliff-Lint"
  echo "---------------------------------------------"
}

### Function Lint #############################################################
Lint() {
  ##########
  # Prints #
  ##########
  echo "---------------------------------------------"
  echo "-- Running lint... ---"
  echo "---------------------------------------------"
  sh -c "xmllint --schema /xliff-core-1.2-strict.xsd --noout $(find $input_paths -name '*.xlf')"
}

#### Function Footer ###########################################################
Footer() {
  ##########
  # Prints #
  ##########
  echo "----------------------------------------------"
  echo "----------------------------------------------"
  echo "The script has completed"
  echo "----------------------------------------------"
  echo "----------------------------------------------"
}

################################################################################
############################### MAIN ###########################################
################################################################################

##########
# Header #
##########
Header

##########
# Lint #
##########
Lint

##########
# Footer #
##########
Footer
